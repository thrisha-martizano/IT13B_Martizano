    private void saveOrderToFIle(String orderSummary) {
        try (FileWriter writer = new FileWriter(FILE, true)) { //Append mode
            writer.write(orderSummary + System.lineSeparator()); //Add a line break
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error saving order to file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private double calculateTotal() {
    double total = 0;
    for (int i = 0; i < cartModel.getRowCount(); i++) {
        try {
            int quantity = Integer.parseInt(cartModel.getValueAt(i, 1).toString());
            double price = Double.parseDouble(cartModel.getValueAt(i, 2).toString());
            total += quantity * price;
        } catch (NumberFormatException | NullPointerException e) {
            // Handle potential errors (e.g., empty cells, non-numeric data)
            JOptionPane.showMessageDialog(null, "Error calculating total. Check cart data.");
            return 0; // or throw an exception
        }
    }
    return total;
}

