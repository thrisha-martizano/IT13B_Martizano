    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int selectedRow = cartTable.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a row to remove.");
        return;
    }

    String flowerName = cartModel.getValueAt(selectedRow, 0).toString();

    // Find and uncheck the corresponding checkbox and set quantity to 0
    switch (flowerName) {
        case "Rosey":
            jCheckBox1.setSelected(false);
            jSpinner1.setValue(0);
            break;
        case "Hearts Glow":
            jCheckBox2.setSelected(false);
            jSpinner2.setValue(0);
            break;
        case "Heather":
            jCheckBox3.setSelected(false);
            jSpinner3.setValue(0);
            break;
        case "Liliana":
            jCheckBox4.setSelected(false);
            jSpinner4.setValue(0);
            break;
        case "Lilies":
            jCheckBox5.setSelected(false);
            jSpinner5.setValue(0);
            break;
        case "Mon Amour":
            jCheckBox6.setSelected(false);
            jSpinner6.setValue(0);
            break;
        case "Melody":
            jCheckBox7.setSelected(false);
            jSpinner7.setValue(0);
            break;
        case "Pink Surprise":
            jCheckBox8.setSelected(false);
            jSpinner8.setValue(0);
            break;
        case "Perfect You":
            jCheckBox9.setSelected(false);
            jSpinner9.setValue(0);
            break;
        case "Sun Blush":
            jCheckBox10.setSelected(false);
            jSpinner10.setValue(0);
            break;
        case "Ever After":
            jCheckBox11.setSelected(false);
            jSpinner11.setValue(0);
            break;
        case "Citrus Grove":
            jCheckBox12.setSelected(false);
            jSpinner12.setValue(0);
            break;
            
        default:
            JOptionPane.showMessageDialog(null, "Error removing item. Unknown flower name.");
            return;
    }

    // Remove the row from the cart table
    cartModel.removeRow(selectedRow);
    
//    // Recalculate and update the total price
    double total = calculateTotal();
    totapricelabel.setText(String.format("%.2f", total));
    }//GEN-LAST:event_jButton3ActionPerformed

