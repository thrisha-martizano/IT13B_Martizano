    private void jplaceorderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jplaceorderActionPerformed
         if (deliveryaddress == null || deliveryaddress.isEmpty() || deliveryaddress.equals("SELECT LOCATION")) {
        JOptionPane.showMessageDialog(null, "Please select a delivery address.", "No location selected", JOptionPane.WARNING_MESSAGE);
        return; // Stop further execution if no address is selected
    }

    StringBuilder summary = new StringBuilder("Order Summary:\n");
    double total = 0;

    for (int i = 0; i < cartModel.getRowCount(); i++) {
        String flowerName = cartModel.getValueAt(i, 0).toString();
        int quantity = Integer.parseInt(cartModel.getValueAt(i, 1).toString());
        double price = Double.parseDouble(cartModel.getValueAt(i, 2).toString());
        double itemTotal = quantity * price;
        summary.append(flowerName).append(": ").append(quantity).append("x ").append(String.format("%.2f", price)).append(" = ").append(String.format("%.2f", itemTotal)).append("\n");
        total += itemTotal;
    }

    summary.append("\nTotal: ").append(String.format("%.2f", total));
    summary.append("\nDelivery Address: ").append(deliveryaddress);

    JOptionPane.showMessageDialog(null, summary.toString(), "Order Summary", JOptionPane.INFORMATION_MESSAGE);
    saveOrderToFIle(summary.toString());
    reset();

    }//GEN-LAST:event_jplaceorderActionPerformed

